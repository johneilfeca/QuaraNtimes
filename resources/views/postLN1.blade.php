	<p class="lead text-justify" style="text-indent: 40px">The Philippines on Thursday, April 7, logged 6,414 new COVID-19 cases, bringing the country's total to 819,164.
    "The relatively lower number of cases today is due to the low number of samples received by laboratories last April 4, 2021," the Department of Health (DOH) said.
    Of the total cases, 19.4% or 158,701 are active or currently sick.</p><br>

