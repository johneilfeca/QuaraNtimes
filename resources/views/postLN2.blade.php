@section('content')
	<br><br>
	<p class="lead text-justify" style="text-indent: 40px">A man in the Philippines has died after being forced to do 300 squats for breaching Covid curfews, making him the latest victim of the country's often brutal approach to enforcing restrictions.</p>

	<p class="lead text-justify" style="text-indent: 40px">On April 1, Darren Manaog Peñaredondo, 28, left his home in General Trias, a city in Cavite province, which is under lockdown due to rising Covid-19 cases, to buy water, his family said, according to CNN affiliate CNN Philippines.</p>
    
	<p class="lead text-justify" style="text-indent: 40px">But he was stopped by police and told to do "pumping exercises" 100 times, according to the report. Police made him repeat the exercises, meaning he ultimately did about 300 repetitions.</p><br>

	<img class="img-fluid" src="img/img-LN2-1.jpg"><br><br><br>
				
	<p class="lead text-justify" style="text-indent: 40px">"He started to convulse on Saturday, but we were able to revive him at home. Then his body failed so we revived him again, but he was already comatose," his family said, according to the report. Peñaredondo died at 10 p.m., the family said.</p>

	<p class="lead text-justify" style="text-indent: 40px">The Philippines has one of the highest reported Covid-19 caseloads of any country in Asia -- it has recorded more than 819,000 infections and 14,000 deaths, according to Johns Hopkins University. Last month, cases in the country rose sharply, prompting authorities to order more than 25 million people into lockdown -- including those in Cavite province.</p>
    
	<p class="lead text-justify" style="text-indent: 40px">The Department of the Interior and Local Government and the mayor of General Trias city have ordered an investigation into Peñaredondo's death, according to the report. "All police officers who will be proven to have violated the law will be prosecuted and meted with appropriate (administrative) and criminal penalties," the department's undersecretary Jonathan Malaya said in a text message to CNN Philippines.</p>
  
	<p class="lead text-justify">Peñaredondo's death follows a string of incidents involving brutal policing techniques.</p>
    
	<p class="lead text-justify" style="text-indent: 40px">In a statement last month, non-profit organization Human Rights Watch (HRW) pointed to reports that officials had locked five youths inside a dog cage for violating quarantine. They also reportedly forced people to sit in the midday sun as punishment for breaching a curfew.</p>
    
	<p class="lead text-justify" style="text-indent: 40px">Jose Manuel Diokno, a lawyer and founder of Free Legal Assistance Group (FLAG), said it was not legal to lock people up in cages or make people squat 300 times. "The only penalties that can be imposed by law enforcers for any kind of violations are those found in local law and national law, and we don't have any laws that allow people to be put in dog cages or be made to exercise for long periods of time," he said.</p><br>

	<h1 class="section-heading">A tough approach to Covid restrictions</h1><br>

	<p class="lead text-justify">The Philippines has taken a tough approach to containing coronavirus.</p>
	
	<p class="lead text-justify" style="text-indent: 40px">President Rodrigo Duterte has applied his traditional strongman tactics, saying in April last year that police would shoot dead anyone who violated virus restrictions. "I will not hesitate. My orders are to the police, the military and the barangays: If they become unruly and they fight you and your lives are endangered, shoot them dead," Duterte said during a speech.</p>
	
	<p class="lead text-justify" style="text-indent: 40px">Huge numbers of people have been detained for breaching restrictions in the past 12 months. Between March and August last year, nearly 290,000 people were warned, fined or charged for violating quarantine rules, CNN Philippines reported. Since Duterte put the main Philippine island of Luzon in lockdown on March 16 this year, hundreds of people had been arrested in Manila, HRW said in March.</p><br>
								
	<img class="img-fluid" src="img/img-LN2-2.jpg">

	<span class="caption text-muted">Police officers inspect motorists at a quarantine checkpoint, on March 29, 2021 in Marikina, Metro Manila, Philippines.</span><br><br>
								
    <p class="lead text-justify" style="text-indent: 40px">Philippine authorities argue the tough approach is needed to control the country's outbreak. But Carlos Conde, a senior researcher at HRW, who is based in the Philippines, argues that surging cases show the measures haven't worked. Instead, he said the decision to arrest people en masse has likely seen people "packed like sardines" into crowded jails, with no social distancing.</p>
	
	<p class="lead text-justify" style="text-indent: 40px">Lockdown orders had also harmed people who need to leave their homes to work, he said, adding the measures were "very anti-poor."</p>
	
	<p class="lead text-justify" style="text-indent: 40px">In its annual report released this week, Amnesty International criticized the Philippines' approach, noting that "measures taken by the government to curb the spread of Covid-19 led to numerous abuses of human rights."</p>
	
	<p class="lead text-justify" style="text-indent: 40px">Last month, Duterte defended using former military officers in the fight against Covid-19, saying, "You need not be a doctor here," according to a CNN Philippines report. CNN has reached out to the official Philippine Information Agency for comment.</p><br>
								
	<h1 class="section-heading">Decline in freedoms</h1><br>

	<p class="lead text-justify" style="text-indent: 40px">Brutal policing methods have been an issue for years in the Philippines. Since Duterte came to power in 2016, thousands have died in the "war on drugs" after the president ordered police to kill anyone they believed to be connected to the drugs trade.</p>
	
	<p class="lead text-justify">But activists say the pandemic has further degraded freedoms and human rights.</p>
	
	<p class="lead text-justify" style="text-indent: 40px">According to Conde, the key problem is the government is treating Covid-19 as a public safety issue -- not a health concern. The outsized roles given to military and police had only increased the prevalence of aggressive policing tactics, he said.</p>
	
	<p class="lead text-justify" style="text-indent: 40px">"I think the police, the military and the local government, they have been emboldened to commit human rights violations even more during the pandemic," he said.</p><br>			
						
	<img class="img-fluid" src="img/img-LN2-3.jpg">

	<span class="caption text-muted">A police officer takes mugshots of alleged curfew violators at a quarantine checkpoint on March 29, 2021 in Marikina, Metro Manila, Philippines.</span><br><br>
							
	<p class="lead text-justify">Diokno, the lawyer, said authorities had "just taken a cue from their leader," referring to Duterte.</p>

	<p class="lead text-justify" style="text-indent: 40px">There have been impacts beyond those who were arrested for breaching quarantine. According to HRW, there was a 50% increase in people killed in the "war on drugs" from April to July 2020 compared with the previous four-month period.</p>
	
	<p class="lead text-justify" style="text-indent: 40px">Diokno said human rights had "very clearly" been degraded over the pandemic. "Aside from the lives that have been lost, the first victims of the pandemic were democratic rights and freedoms," he said.</p><br><br><br>

